// operator perbandingan digunakan untuk membandingkan dua buah operand dan mengembalikan nilai true atau false

// 1. sama dengan (==) : membandingkan dua nilai sama atau tidak
// 1. tidak sama dengan (!=) : membandingkan dua nilai apakah mereka tidak smaa
// 3. identik (===) : memabndingkan dua operand atau nilai beserta tipe datanya, apakah nilai dan tipe datanya sama atau tidak
// 4. tidak identik (!==) : memabndingkan dua operand atau nilai beserta tipe datanya, apakah nilai dan tipe datanya tidak sama
// 5. kurang dari (<) : membandingkan dua nilai apakah nilai kiri lebih kecil dari nilai kanan
// 6. lebih dari (>) : membandingkan dua nilai apakah nilai kiri lebih besar dari nilai kanan
// 7. kurang dari sama dengan (<=) : membandingkan dua nilai apakah nilai kiri lebih kecil atau sama dengan nilai kanan
// 8. lebih dari sama dengan (>=) : membandingkan dua nilai apakah nilai kiri lebih besar atau sama dengan nilai kanan

const a = 5;
const b = 2;

console.log(a == b);// false
console.log(a != b);// true
console.log(a === b);// false
console.log(a !== b);// true
console.log(a < b);// false
console.log(a > b);// true
console.log(a <= b);// false
console.log(a >= b);// true



