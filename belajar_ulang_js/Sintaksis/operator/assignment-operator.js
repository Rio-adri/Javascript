// assignment operator adalah operator yang digunakan untuk memberikan suatu nilai dan mengubah yaitu ditanndai dengan (=)
// += ditambahkan dan kangsung didefinisikan nilainya
// -= dikuarang dan kangsung didefinisikan nilainya
// *= dikali dan kangsung didefinisikan nilainya
// /= dibagi dan kangsung didefinisikan nilainya
let nama = 'rio';

console.log(nama);

nama = 'aji';

console.log(nama);

let angka1 = 9;
let angka2 = 1;

console.log(angka1 += angka2);// output 10
console.log(angka1 -= angka2);// output 9
console.log(angka1 *= angka2);// output 9
console.log(angka1 /= angka2);// output 9



