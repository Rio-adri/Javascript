// operator aritmatika adalah operator yang digunakan untuk melakukan operasi aritmatika.
// Adapun simbol nya yaitu : 
// + untuk pertambahan
// - untuk pengurangan
// * untuk perkalian
// / untuk pembagian 
//  % untuk sisa bagi
// ++(increment) untuk menambah satu misalnya ++3 maka akan jadi 4
// --(decrement) untuk mengurangi nilai sebanyak 1. misalnya --3 maka akan jadi 2
// ** untuk perpangkatan

const tambah = 2 + 2 ;
const kurang = 2 - 2 ;
const kali = 2 * 2 ;
const bagi = 2 / 2 ;
const modulus = 2 % 2 ;



// perpangkatan
const perpangkatan = 2**2 ;


console.log(tambah);
console.log(kurang);
console.log(kali);
console.log(bagi);
console.log(modulus);

console.log(perpangkatan);


