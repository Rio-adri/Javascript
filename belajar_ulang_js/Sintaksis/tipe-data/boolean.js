// boolean adalah tipe data yang hanya memiliki dua nilai yaitu true dan false
// true : adalah iya 
// false : adalah tidak
// contoh kode : 

const complete = true;
const notComplete = false;

console.log(complete,notComplete);
// output : true dan false

// nilai true ataupun false bisa didapatkan dari hasil operator perbandingan, misalnya : 

const angka1 = 5;
